
/*
 Write a function with the name Double.
Create a variable answer as numeric data type.
Function takes paisa as input parameter.
Multiply paisa with 2 and store the result in answer.
Return answer. 
call the function multiple times with different parameters.
*/
//function declaration
function double(paisa) {
    let answer = paisa * 2;
    return answer;
}

